@dataclass(frozen=True)
class Token:
    kind: TokenKind
    value: Any = None
    range: Range | None = None
    text: str = ""

    def with_kind(self, kind: TokenKind, value: Any = None) -> Token:
        return Token(kind, self.value if value is None else value, self.range, self.text)
